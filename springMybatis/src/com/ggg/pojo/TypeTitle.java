package com.ggg.pojo;

import java.util.List;

public class TypeTitle {
	private String ttid;
	private String ttname;
	private String typeid;
	private List<TypeGoods> tglist;
	
	public List<TypeGoods> getTglist() {
		return tglist;
	}
	public void setTglist(List<TypeGoods> tglist) {
		this.tglist = tglist;
	}
	public String getTtid() {
		return ttid;
	}
	public void setTtid(String ttid) {
		this.ttid = ttid;
	}
	public String getTtname() {
		return ttname;
	}
	public void setTtname(String ttname) {
		this.ttname = ttname;
	}
	public String getTypeid() {
		return typeid;
	}
	public void setTypeid(String typeid) {
		this.typeid = typeid;
	}
	
	
}
